    private void btnThoiViecActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThoiViecActionPerformed
        int row=tblNhanVien.getSelectedRow();
        if (row >= 0) {
            NhanVienDTO nv=new NhanVienDTO();
            nv= NhanVienBUS.DSNV.get(row);
            if(nv.getTTLamviec()==(false)){
                            System.out.println(nv);
                bus.setTTLamviec(nv.getMaNV(),"true");
                tblNhanVien.setValueAt("true", row, 7);
            }
            else {
                bus.setTTLamviec(nv.getMaNV(),"false");
                tblNhanVien.setValueAt("false", row, 7);
            }
        }
       
    }//GEN-LAST:event_btnThoiViecActionPerformed

    private void tblNhanVienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblNhanVienMouseClicked
        ListSelectionModel lsm=tblNhanVien.getSelectionModel();
        lsm.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        int row = tblNhanVien.getSelectedRow();
        if (row >= 0) {
            txtMaNV.setText(String.valueOf(tblNhanVien.getValueAt(row, 0)));
            txtHoNV.setText(String.valueOf(tblNhanVien.getValueAt(row, 1)));
            txtTenNV.setText(String.valueOf(tblNhanVien.getValueAt(row, 2)));
            txtEmailNV.setText(String.valueOf(tblNhanVien.getValueAt(row, 3)));
            txtSDT.setText(String.valueOf(tblNhanVien.getValueAt(row, 4)));
            txtChucvu.setText(String.valueOf(tblNhanVien.getValueAt(row, 6)));
            String g=String.valueOf(tblNhanVien.getValueAt(row, 5));
            if ("Nam".equals(g)) btnNam.setSelected(true);
            else if("Nữ".equals(g)) btnNu.setSelected(true);
            txtMaNV.setEditable(false);
            txtMaNV.setBackground(new Color(229, 228, 228));
            btnThem.setEnabled(false); 
        }
    }//GEN-LAST:event_tblNhanVienMouseClicked

