    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        if(txtMaNV.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Mã nhân viên không được để trống!", "Thông báo", JOptionPane.ERROR_MESSAGE);
            return;
        }
        for(NhanVienDTO nv: NhanVienBUS.DSNV){
            if(txtMaNV.getText().equals(String.valueOf(nv.getMaNV()))){
                JOptionPane.showMessageDialog(this, "Mã nhân viên đã tồn tại!", "Thông báo", JOptionPane.ERROR_MESSAGE);
                return;
            } 
        }  
        String gt="";
        gt=btnNam.isSelected()?"Nam":"Nữ";
        NhanVienDTO nv=new NhanVienDTO(txtMaNV.getText(),
                txtHoNV.getText(),
                txtTenNV.getText(),
                txtEmailNV.getText(),
                txtSDT.getText(),
                gt, 
                txtChucvu.getText());
        bus.themNV(nv);
        model.addRow(new Object[]{nv.getMaNV(), nv.getHoNV(),
            nv.getTenNv(),nv.getEmailNV(),
            nv.getSdtNV(),nv.getGtinhNV(), 
            nv.getChucVu(),"true"
        });
    }//GEN-LAST:event_btnThemActionPerformed

