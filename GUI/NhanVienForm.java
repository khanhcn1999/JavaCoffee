    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        String gt="";
        gt=btnNam.isSelected()?"Nam":"Nữ";
        int row=tblNhanVien.getSelectedRow();
        NhanVienDTO nv=new NhanVienDTO(txtMaNV.getText(),
                txtHoNV.getText(),
                txtTenNV.getText(),
                txtEmailNV.getText(),
                txtSDT.getText(),
                gt, 
                txtChucvu.getText());
        bus.suaNV(row, nv);
        model.setValueAt(nv.getHoNV(), row, 1);
        model.setValueAt(nv.getTenNv(), row, 2);
        model.setValueAt(nv.getEmailNV(), row, 3);
        model.setValueAt(nv.getSdtNV(), row, 4);
        model.setValueAt(nv.getGtinhNV(), row, 5);
        model.setValueAt(nv.getChucVu(), row, 6);
        model.setValueAt(nv.getTTLamviec(), row, 7);
        reset();
    }//GEN-LAST:event_btnSuaActionPerformed

    private void txtTimKiemKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtTimKiemKeyPressed
        if(evt.getKeyCode()==KeyEvent.VK_ENTER){
            if(txtTimKiem.getText().equals(""))
            {
                JOptionPane.showMessageDialog(this, "vui lòng nhập dữ liệu tìm kiếm!");
                txtTimKiem.requestFocus();
            }
            else {
                ArrayList<NhanVienDTO> temp=new ArrayList<>();
                temp=bus.timKiem(txtTimKiem.getText());
                if(temp!=null) showDuLieu(temp);
                else lbTimKiem.setText("Không tìm thấy dũ liệu ");
            }
        }//GEN-LAST:event_txtTimKiemKeyPressed
    }
